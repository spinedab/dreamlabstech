def build_svg(slug: str, c1: str, c2: str, icon_path: str) -> str:
    safe_id = re.sub(r"[^a-zA-Z0-9_-]", "_", slug)
    return f"""<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512" role="img" aria-label="{slug} logo">
  <defs>
    <linearGradient id="bg_{safe_id}" x1="0" y1="0" x2="1" y2="1">
      <stop offset="0%" stop-color="{c1}"/>
      <stop offset="100%" stop-color="{c2}"/>
    </linearGradient>
    <filter id="shadow_{safe_id}" x="-20%" y="-20%" width="140%" height="140%">
      <feDropShadow dx="0" dy="4" stdDeviation="8" flood-color="#000000" flood-opacity="0.25"/>
    </filter>
  </defs>
  <rect width="512" height="512" rx="96" fill="url(#bg_{safe_id})"/>
  <g transform="translate(128,128) scale(1.0)" fill="white" filter="url(#shadow_{safe_id})">
    {icon_path}
  </g>
</svg>
"""


def read_inventory(path: Path) -> list[tuple[str, str, str, str]]:
    rows: list[tuple[str, str, str, str]] = []
    if path.exists():
        with path.open("r", encoding="utf-8") as inventory:
            for line in inventory:
                line = line.strip()
                if not line or line.startswith("#"):
                    continue
                parts = line.split("|")
                while len(parts) < 4:
                    parts.append("")
                slug, package_id, category = parts[0].strip(), parts[1].strip(), parts[2].strip()
                description = "|".join(parts[3:]).strip()
                if slug:
                    rows.append((slug, package_id, category or FALLBACK_CATEGORY, description))
    if rows:
        return rows
    return [(slug, f"com.dreamlabstech.{slug}", category, "") for slug, category in SLUG_CATEGORY.items()]


def select_icon(slug: str, category: str) -> tuple[str, bool]:
    if slug in APP_ICON_MAP:
        return APP_ICON_MAP[slug], False
    return CATEGORY_FALLBACK_ICONS.get(category, FALLBACK_ICON), True


def write_svgs(rows: list[tuple[str, str, str, str]]) -> tuple[int, Counter, list[str]]:
    LOGOS_DIR.mkdir(parents=True, exist_ok=True)
    category_counts: Counter = Counter()
    fallback_slugs: list[str] = []

    for slug, _package_id, category, _description in rows:
        category_counts[category] += 1
        c1, c2 = PALETTE.get(category, PALETTE[FALLBACK_CATEGORY])
        icon_name, used_fallback = select_icon(slug, category)
        if used_fallback:
            fallback_slugs.append(slug)
        icon_path = ICON_PATHS.get(icon_name, ICON_PATHS[FALLBACK_ICON])
        (LOGOS_DIR / f"{slug}.svg").write_text(build_svg(slug, c1, c2, icon_path), encoding="utf-8")

    return len(rows), category_counts, fallback_slugs


def write_pngs(rows: list[tuple[str, str, str, str]]) -> int:
    ICONS_DIR.mkdir(parents=True, exist_ok=True)
    try:
        import cairosvg  # type: ignore
    except Exception as exc:
        print(f"[generate_logos] cairosvg unavailable ({exc}); trying rsvg-convert")
        rsvg = shutil.which("rsvg-convert")
        if rsvg:
            return _png_via_rsvg(rsvg, rows)
        print("[generate_logos] No SVG-to-PNG converter available; skipping PNG generation")
        return 0

    ok = 0
    for slug, _package_id, _category, _description in rows:
        try:
            cairosvg.svg2png(
                url=str(LOGOS_DIR / f"{slug}.svg"),
                write_to=str(ICONS_DIR / f"{slug}.png"),
                output_width=512,
                output_height=512,
            )
            ok += 1
        except Exception as exc:
            print(f"[generate_logos] PNG failed for {slug}: {exc}")
    return ok


def _png_via_rsvg(rsvg: str, rows: list[tuple[str, str, str, str]]) -> int:
    ok = 0
    for slug, _package_id, _category, _description in rows:
        try:
            subprocess.run(
                [rsvg, "-w", "512", "-h", "512", "-o", str(ICONS_DIR / f"{slug}.png"), str(LOGOS_DIR / f"{slug}.svg")],
                check=True,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.PIPE,
            )
            ok += 1
        except subprocess.CalledProcessError as exc:
            err = exc.stderr.decode(errors="ignore").strip()
            print(f"[generate_logos] rsvg-convert failed for {slug}: {err}")
    return ok


def verify_svgs(rows: list[tuple[str, str, str, str]]) -> None:
    missing_paths: list[str] = []
    with_text: list[str] = []
    for slug, _package_id, _category, _description in rows:
        svg = (LOGOS_DIR / f"{slug}.svg").read_text(encoding="utf-8")
        if "<path" not in svg:
            missing_paths.append(slug)
        if "<text" in svg:
            with_text.append(slug)
    if missing_paths or with_text:
        raise RuntimeError(
            f"SVG verification failed; missing paths={missing_paths}, text elements={with_text}"
        )


def main() -> int:
    rows = read_inventory(INVENTORY)
    total, category_counts, fallback_slugs = write_svgs(rows)
    png_count = write_pngs(rows)
    verify_svgs(rows)

    print(f"[generate_logos] SVGs generated: {total}")
    print(f"[generate_logos] PNGs generated: {png_count}")
    print(f"[generate_logos] Explicit icon mappings: {total - len(fallback_slugs)}")
    print(f"[generate_logos] Category fallbacks: {len(fallback_slugs)}")
    print("[generate_logos] Categories: " + ", ".join(sorted(category_counts)))
    if fallback_slugs:
        print("[generate_logos] Fallback slugs: " + ", ".join(fallback_slugs))
    return 0


if __name__ == "__main__":
    sys.exit(main())
